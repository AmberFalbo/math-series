def fibonacci(n):
    """This is the fibonacci function

    Args:
        n ([type]): [description]
    """
    if n < 0:
      print("Error, try a new number.")
    
    elif n == 0:
        return 0
    elif n == 1:
        return 1
    else:
        return fibonacci(n - 1) + fibonacci(n - 2)


